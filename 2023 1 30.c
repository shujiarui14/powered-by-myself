#include <stdio.h>
// 原码（按位取反）= 反码（+1）=补码
// 原码：  10000000000000000000000000000001
// 反码:   11111111111111111111111111111110
// 补码：  11111111111111111111111111111111  
int main()
{
    short a=8;
    int c;
    c=a>>1;
    short b=~a;//~是按位取反，b是有符号的整形
    printf("%d\n",b);//打印的是原码，负数内存中储存的是补码
    printf("%d\n",c);
    return 0;
}